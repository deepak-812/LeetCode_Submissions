class Solution{
  public:
    //Function to sort the given linked list using Merge Sort.
    Node* mergeSort(Node* head) {
        // your code here
        Node* start=NULL,*last=NULL,*curr=head;
        if(!head){
            return head;
        }
        vector<int> v;
        while(curr){
            v.push_back(curr->data);
            curr=curr->next;
        }
        sort(v.begin(),v.end());
        for(int i=0;i<v.size();i++){
            int val=v[i];
            Node* nd=new Node(val);
            if(!start){
                start=nd;
            }
            else{
                last->next=nd;
            }
            last=nd;
        }
        return start;
    }
};


